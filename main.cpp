// C++ program to convert binary to decimal
// when input is represented as binary string.
#include <iostream>
#include <string>
using namespace std;

// Function to convert binary to decimal
int binaryToDecimal(string str)
{
	int dec_num = 0;
	int power = 0 ;
	int n = str.length() ;

	for(int i = n-1 ; i>=0 ; i--){
	if(str[i] == '1'){
		dec_num += (1<<power) ;
	}
	power++ ;
	}

	return dec_num;
}

